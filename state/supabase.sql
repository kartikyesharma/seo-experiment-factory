create extension if not exists pgcrypto;

create table if not exists public.leads (
  id uuid primary key default gen_random_uuid(),
  site_id text not null,
  email text not null,
  source text not null,
  consented_at timestamptz not null,
  created_at timestamptz not null default now(),
  unique (site_id, email)
);

create table if not exists public.product_catalog (
  site_id text not null,
  product_id text not null,
  provider_link_reference text not null unique,
  title text not null,
  amount_minor integer not null check (amount_minor between 9900 and 49900),
  currency text not null default 'INR',
  private_pdf_key text not null,
  email_subject text not null,
  email_body text not null,
  active boolean not null default false,
  primary key (site_id, product_id)
);

create table if not exists public.orders (
  id uuid primary key default gen_random_uuid(),
  provider text not null check (provider in ('razorpay', 'cashfree')),
  provider_payment_id text not null unique,
  site_id text not null,
  product_id text not null,
  customer_email text not null,
  amount_minor integer not null check (amount_minor > 0),
  currency text not null default 'INR',
  status text not null,
  delivered_at timestamptz,
  raw_event jsonb not null,
  created_at timestamptz not null default now()
);

create table if not exists public.page_metrics (
  id bigint generated by default as identity primary key,
  site_id text not null,
  metric_date date not null,
  page text not null,
  query text not null,
  clicks integer not null default 0,
  impressions integer not null default 0,
  ctr numeric not null default 0,
  position numeric not null default 0,
  unique (site_id, metric_date, page, query)
);

create table if not exists public.experiment_suggestions (
  id uuid primary key default gen_random_uuid(),
  site_id text not null,
  page text not null,
  query text,
  reason text not null,
  evidence jsonb not null,
  status text not null default 'review' check (status in ('review', 'approved', 'rejected', 'shipped')),
  created_at timestamptz not null default now()
);

alter table public.leads enable row level security;
alter table public.product_catalog enable row level security;
alter table public.orders enable row level security;
alter table public.page_metrics enable row level security;
alter table public.experiment_suggestions enable row level security;

-- No anonymous policies are created. These tables are written through n8n with a
-- protected server-side service credential. Add narrowly-scoped read policies only
-- if an authenticated admin UI is introduced.
